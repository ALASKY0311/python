#터틀로 다양한 도형 그리기
import turtle as t

t.shape("turtle")
t.color("blue")
t.speed(0)
t.left(90)
t.forward(400)
t.backward(800)
t.forward(400)
t.left (90)
t.forward(400)
t.backward(800)
t.up()
t.goto(100,100)
t.down()
for x in range(4):
    t.right(90)
    t.forward(100)
t.up()
t.goto(-100,100)
t.down()
for x in range(5):
    t.forward(100)
    t.right(120)
t.up()
t.goto(-100,-100)
t.right(72)
t.down()
for x in range(4):
    t.forward(100)
    t.right(72)
t.forward(100)
t.up()
t.goto(200,-100)
t.down()
for x in range(4):
    t.forward(200)
    t.right(144)
t.forward(200)
